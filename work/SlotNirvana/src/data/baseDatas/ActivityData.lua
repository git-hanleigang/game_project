-- 引用名删除活动数据
function ActivityData:delActivityDataByRef(refName)
    if not refName then
        return nil
    end

    -- 发送结束时间
    local value = self.m_activityDatas[refName]
    if value then
        local _data = nil
        if #value > 0 then
            _data = value[1]
        else
            _data = value
        end

        local _theme = _data:getThemeName()
        if _data then
            gLobalNoticManager:postNotification(ViewEventType.NOTIFY_ACTIVITY_TIMEOUT, {id = _data:getID(), name = refName, theme = _theme})
            if _data:isCanDelete() then
                self.m_activityDatas[refName] = nil
            end
        end
    end
end

