-- 创建活动数据对象
function ActivityData:createActivityData(ref)
    ref = "" .. ref
    local _filePath = ActivityDataType[ref]
    if not _filePath then
        -- 不存在配置的活动数据类型，使用默认的
        -- _filePath = "baseActivity.BaseActivityData"
        return nil
    elseif _filePath == "" then
        _filePath = "baseActivity.BaseActivityData"
    end

    local createFunc = function()
        -- 模块资源存在  保存数据
        local activityData = require(_filePath):create()

        return activityData
    end

    -- 异常处理方式执行func
    local status, result = xpcall(createFunc, debug.traceback)
    if status then
        return result
    else
        if isMac() then
            local errMsg = ref .. " ==> " .. tostring(result)
            assert(false, errMsg)
        end
        return nil
    end
end

-- id获取活动数据
function ActivityData:getActivityDataById(activityId)
    if not activityId or activityId == "" then
        return nil
    end
    
    for key, value in pairs(self.m_activityDatas) do
        if #value > 0 then
            for i = 1, #value do
                local _activityData = value[i]
                local _activityId = _activityData:getID()
                if _activityId == activityId then
                    return _activityData
                end
            end
        else
            local _activityData = value
            local _activityId = value:getID()
            if _activityId == activityId then
                return _activityData
            end
        end
    end

    return nil
end

