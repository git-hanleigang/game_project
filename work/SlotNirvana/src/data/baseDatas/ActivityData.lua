--多档促销
function ActivityData:parsMultiSale(data)
    if #data.choices == 0 and #data.newUserChoices == 0 then
        return 
    end

    local bNovice = false 
    local choices = data.choices
    if #data.newUserChoices > 0 then
        bNovice = true
        choices = data.newUserChoices
    end
    
    -- 检查促销是否可用
    local bSucces = true
    for i = 1, #choices do
        local _data = choices[i]
        local config = nil
        if bNovice then
            config = G_GetMgr(G_REF.UserNovice):getActivityConfigById(_data.activityId)
        else
            config = globalData.GameConfig:getActivityConfigById(_data.activityId, ACTIVITY_TYPE.CHOICE)
        end
        if not config then
            bSucces = false
            break
        end
    end
    if not bSucces then
        printInfo("配置中不存在多档促销数据！！")
        return
    end

    self:parsePromotionDataByRef(choices, ACTIVITY_REF.MultiSale, ACTIVITY_TYPE.CHOICE, bNovice)
