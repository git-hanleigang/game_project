-- 引用名获取活动
function GD.G_GetActivityDataByRef(refName, notCheckRunning)
    if globalData.commonActivityData then
        local _data = globalData.commonActivityData:getActivityDataByRef(refName)
        if _data then
            if notCheckRunning then
                return _data
            elseif not notCheckRunning and _data.isRunning and _data:isRunning() then
                return _data
            else
                return nil
            end
        end
    else
        return nil
    end
end

-- 引用名删除活动
function GD.G_DelActivityDataByRef(refName)
    if globalData.commonActivityData then
        globalData.commonActivityData:delActivityDataByRef(refName)
    end
end

