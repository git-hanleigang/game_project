--开启刷帧 剩余时间是倒计时类型放到这里
function ActivityData:startUpdate()
    self:stopUpdate()

    self.m_expireALLHandlerId =
        scheduler.scheduleGlobal(
        function()
            if self.m_pauseUpdate then
                return
            end

            local GashaponMgr = G_GetMgr(ACTIVITY_REF.Gashapon)
            if GashaponMgr then
                GashaponMgr:updateGashaponBigRewardCount()
            end
            local IcebreakerSaleCtrl = G_GetMgr(G_REF.IcebreakerSale)
            if IcebreakerSaleCtrl then
                IcebreakerSaleCtrl:checkTimeOut()
            end
            local LuckyRaceMgr = G_GetMgr(ACTIVITY_REF.LuckyRace)
            if LuckyRaceMgr then
                LuckyRaceMgr:checkOpenEntry()
            end
            for key, value in pairs(self.m_activityDatas) do
                if value then
                    local _data = nil
                    if #value > 0 then
                        _data = value[1]
                    else
                        _data = value
                    end

                    if _data then
                        local expaireAt = _data:getExpireAt()
                        -- 活动到期
                        if expaireAt > 0 then
                            -- 是否设置成完成
                            if _data:isRunning() and not _data:isCompleted() and _data:checkCompleteCondition() then
                                _data:setCompleted(true)
                                -- 发送完成事件
                                gLobalNoticManager:postNotification(ViewEventType.NOTIFY_ACTIVITY_COMPLETED, {id = _data:getID(), name = _data:getRefName()})
                            end

                            if not (_data:getLeftTime() > 0) and not _data:isIgnoreExpire() and _data:isCanDelete() then
