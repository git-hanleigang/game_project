JungleJauntPublicConfig.isCanOpenChooseView = function(_mainClass)
    if _mainClass.m_chooseView and _mainClass.m_chooseView:isVisible() then
        return false
    end
    -- free
    if _mainClass.m_bProduceSlots_InFreeSpin then
        return false
    end
    -- 滚轮转动
    if _mainClass:getGameSpinStage() ~= IDLE or _mainClass:getCurrSpinMode() ~= NORMAL_SPIN_MODE then
        return false
    end
    -- 事件执行
    if _mainClass.m_isRunningEffect then
        return false
    end

    return true
end

