local getRandomValue = function(_v,_pro)
    local total = 0
    local proList = {}
    for i=1,#_pro do
        total = total + _pro[i]
        table.insert(proList,total)
    end
    local random = math.random(1,total)
    for i=1,#proList do
        if random >= proList[i] then
            return _v[i]
        end
    end
     
end

